package com.riambsoft.framework.core.web.access;

public class RSAccessException extends Exception {

	private static final long serialVersionUID = -8155920313467645027L;

	public RSAccessException() {
		super();
	}

	public RSAccessException(String message, Throwable cause) {
		super(message, cause);
	}

	public RSAccessException(String message) {
		super(message);
	}

	public RSAccessException(Throwable cause) {
		super(cause);
	}

}
